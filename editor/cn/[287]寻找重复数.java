class Solution_287 {
    public int findDuplicate(int[] nums) {
        int fast = 0;
        int slow = 0;
        fast = nums[nums[fast]];
        slow = nums[slow];
        while (fast != slow) {
            fast = nums[nums[fast]];
            slow = nums[slow];
        }
        fast = 0;
        while (nums[slow] != nums[fast]) {
            fast = nums[fast];
            slow = nums[slow];
        }
        return nums[slow];

    }
}
